#Access Items
thislist = ["apple", "banana", "cherry"]
print(thislist[1]) #prints secont item

#Negative indexing
print(thislist[-1]) 

#Range of indexes
thislist = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(thislist[2:5])

print(thislist[:4])

print(thislist[2:])

print(thislist[-4:-1])

